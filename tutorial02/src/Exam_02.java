/** 
 * 10+20 + 을 계산하여 그 결과를 출력.
 */
public class Exam_02 {
	public static void main(String[] ar){
		int su1=10,su2=20;
		int su3=su1+su2;
		/*
		 * '=' 대입연산자, 치환연산자
		 * 변수=상수(값)
		 * 변수=변수
		 * 변수=수식(숫자를 이용한 계산식 +산술식, 논리식, 관계식...)
		 */
		System.out.println("su1+su2 = "+su3);
		System.out.println(su1+"+"+su2+"="+su3);
	}
}
